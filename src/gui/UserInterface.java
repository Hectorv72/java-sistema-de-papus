    private void jButton3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton3MouseClicked
        // TODO add your handling code here:
        int posicion = this.JListDatos.getSelectedIndex();
        // System.out.println(posicion);
        if(posicion != -1){
            int respuesta = JOptionPane.showConfirmDialog(this.rootPane, "Flaco estas seguro de esto?");
            
            
            if(respuesta == 0 ){
                int id = id_posiciones.get(posicion);
                
                try{
                    controller.eliminarDatos(id);
                }catch(Exception e){
                    System.out.println(e.getMessage());
                }
            }
            
            

            mostrar();
        }
        
    }//GEN-LAST:event_jButton3MouseClicked

