    public void mostrar(){
        
//        PeluqueriaController list_controller = new PeluqueriaController();
        List<Peluqueria> lista = controller.ListarDatos();
        id_posiciones.clear();
        
        DefaultListModel datos = new DefaultListModel();
        for( int i=0; i < lista.size(); i++){
            
            Peluqueria iterador = lista.get(i);
            id_posiciones.add(iterador.getClienteN());
            datos.addElement(iterador.getNombre() + " - " + iterador.getRaza() + " - " + iterador.getColor() );
        }
        this.JListDatos.setModel(datos);
        // System.out.println(id_posiciones);
    }
