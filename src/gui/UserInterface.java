    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        // TODO add your handling code here:
        int clienteN         = 0;
        if(this.JTextFieldClienteN.getText() != ""){
            clienteN = parseInt(this.JTextFieldClienteN.getText());
        }
        
        String nombre        = this.JTextNombre.getText();
        String raza          = this.JTextFieldRaza.getText();
        String color         = this.JTextFieldColor.getText();
        String alergico      = this.JComboBoxAlergico.getSelectedItem().toString();
        String atencionEsp   = this.JComboBoxAtencion.getSelectedItem().toString();
        String nombreDuenio  = this.JTextFieldNombreDuenio.getText();
        String celDuenio     = this.JTextFieldCelDuenio.getText();
        String observaciones = this.JTextAreaObersvaciones.getText();
        
        if(
           clienteN != 0 &&
           nombre != "" &&
           raza != "" &&
           color != "" &&
           nombreDuenio != "" &&
           celDuenio != "" &&
           observaciones != ""
        )
        {
            Peluqueria datos = new Peluqueria(
              clienteN, nombre, raza, color, alergico,
              atencionEsp, nombreDuenio, celDuenio, observaciones
            );
            
            try{
                this.controller.crearDatos(datos);
                limpiarTextFields();
                mostrar();
                
            }catch (Exception e) {
                System.out.println(e.getMessage());
            } 
            
        }
        
        
    }//GEN-LAST:event_jButton1MouseClicked

